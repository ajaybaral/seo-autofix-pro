    /**
     * Apply fixes for selected broken links
     * 
     * @param array $entry_ids Entry IDs to fix
     * @param string $custom_url Optional custom URL to use for replacement
     * @return array Result with counts
     */
    public function apply_fixes($entry_ids, $custom_url = '')
    {
        \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Starting with entry IDs: ' . print_r($entry_ids, true));
        \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Custom URL provided: ' . $custom_url);

        $fixed_count = 0;
        $failed_count = 0;
        $skipped_count = 0;
        $messages = array();

        foreach ($entry_ids as $entry_id) {
            \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Processing entry ID: ' . $entry_id);

            // Get entry details
            $entry = $this->db_manager->get_entry($entry_id);

            if (!$entry) {
                \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Entry not found: ' . $entry_id);
                $failed_count++;
                $messages[] = sprintf(
                    __('Entry #%d not found', 'seo-autofix-pro'),
                    $entry_id
                );
                continue;
            }

            \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Entry data: ' . print_r($entry, true));

            // Check if this is a template-generated link that can't be fixed
            if ($this->is_template_generated_link($entry['broken_url'])) {
                \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Skipping template-generated link: ' . $entry['broken_url']);
                $skipped_count++;
                $messages[] = sprintf(
                    __('⚠️ Cannot fix: %s - This link is generated by WordPress theme/templates. Please fix it in your theme files or comment settings.', 'seo-autofix-pro'),
                    esc_url($entry['broken_url'])
                );
                continue;
            }

            // Determine replacement URL priority: custom_url > user_modified_url > suggested_url
            $replacement_url = '';

            if (!empty($custom_url)) {
                $replacement_url = $custom_url;
                \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Using custom URL from parameter: ' . $replacement_url);
            } elseif (!empty($entry['user_modified_url'])) {
                $replacement_url = $entry['user_modified_url'];
                \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Using user_modified_url from database: ' . $replacement_url);
            } elseif (!empty($entry['suggested_url'])) {
                $replacement_url = $entry['suggested_url'];
                \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Using suggested_url from database: ' . $replacement_url);
            }

            // If still no replacement URL and it's external, skip (only if no custom URL provided)
            if (empty($replacement_url)) {
                if ($entry['link_type'] === 'external') {
                    \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Skipping external link with no replacement: ' . $entry['broken_url']);
                    $skipped_count++;
                    $messages[] = sprintf(
                        __('⚠️ Skipped external link: %s (manual intervention required)', 'seo-autofix-pro'),
                        esc_url($entry['broken_url'])
                    );
                    continue;
                }

                \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] No replacement URL available for: ' . $entry['broken_url']);
                $failed_count++;
                $messages[] = sprintf(
                    __('❌ No replacement URL for: %s', 'seo-autofix-pro'),
                    esc_url($entry['broken_url'])
                );
                continue;
            }

            // Apply fix to the content
            \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Attempting to replace link. Found on: ' . $entry['found_on_url'] . ', Broken: ' . $entry['broken_url'] . ', Replacement: ' . $replacement_url);

            // Pass link_location through so the 4-layer engine can route correctly
            $location     = isset($entry['link_location']) ? $entry['link_location'] : 'content';
            $builder_type = isset($entry['builder_type']) ? $entry['builder_type'] : null;

            \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Link location: ' . $location . ' | Stored builder: ' . ($builder_type ?: 'null'));

            // --- Per-post lock to prevent race conditions ---
            $post_id  = $this->get_post_id_from_url($entry['found_on_url']);
            $lock_key = $post_id ? 'seoautofix_lock_' . $post_id : null;

            if ($lock_key && get_transient($lock_key)) {
                \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] ⚠️ Lock active for post ' . $post_id . ' — skipping entry ' . $entry_id);
                $skipped_count++;
                $messages[] = sprintf(
                    __('⚠️ Skipped: %s — another operation in progress', 'seo-autofix-pro'),
                    esc_url($entry['broken_url'])
                );
                continue;
            }

            if ($lock_key) {
                set_transient($lock_key, time(), 10);
            }

            $verified        = false;
            $manual_required = false;
            $manual_reason   = '';
            $attempts        = 0;
            $max_attempts    = 2;

            try {
                for ($attempt = 1; $attempt <= $max_attempts; $attempt++) {
                    $attempts = $attempt;

                    if ($attempt > 1) {
                        \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] ⏳ Retry #' . ($attempt - 1) . ' after 150ms delay...');
                        usleep(150000);
                    }

                    // Single call into the 4-layer builder engine (location-aware)
                    $success = $this->replace_link_in_content(
                        $entry['found_on_url'],
                        $entry['broken_url'],
                        $replacement_url,
                        $location,
                        $manual_required,
                        $manual_reason
                    );

                    // If the engine flagged manual_required, no point retrying
                    if ($manual_required) {
                        break;
                    }

                    \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Attempt ' . $attempt . ' replacement result: ' . ($success ? 'SUCCESS' : 'FAILED'));

                    if (!$success) {
                        continue;
                    }

                    // Verify the fix with normalized, builder-aware check
                    \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Verifying fix (attempt ' . $attempt . ')...');
                    $verified = $this->verify_fix_applied($entry['found_on_url'], $entry['broken_url'], $location, $builder_type);

                    if ($verified) {
                        \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] ✅ VERIFICATION PASSED on attempt ' . $attempt);
                        break;
                    }

                    \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] ❌ VERIFICATION FAILED on attempt ' . $attempt);
                }

                // --- TRANSACTIONAL: Only update table state AFTER verified success ---
                if ($verified) {
                    $fixed_count++;

                    // Clear caches FIRST so state is immediately visible
                    if ($post_id) {
                        Builder_Replacement_Engine::clear_all_caches($post_id, 'replace');
                    }

                    $mark_result = $this->db_manager->mark_as_fixed($entry_id);
                    \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Mark as fixed result for ID ' . $entry_id . ': ' . ($mark_result ? 'SUCCESS' : 'FAILED'));

                    // Log activity for reporting
                    global $wpdb;
                    $table_activity = $wpdb->prefix . 'seoautofix_broken_links_activity';

                    $wpdb->insert($table_activity, array(
                        'scan_id' => $entry['scan_id'],
                        'entry_id' => $entry_id,
                        'broken_url' => $entry['broken_url'],
                        'replacement_url' => $replacement_url,
                        'action_type' => $custom_url ? 'replaced' : 'fixed',
                        'page_url' => $entry['found_on_url'],
                        'page_title' => $entry['found_on_page_title']
                    ), array('%s', '%d', '%s', '%s', '%s', '%s', '%s'));

                    $messages[] = sprintf(
                        __('✅ Fixed: %s → %s (verified, %d attempt(s))', 'seo-autofix-pro'),
                        esc_url($entry['broken_url']),
                        esc_url($replacement_url),
                        $attempts
                    );
                } elseif ($manual_required) {
                    // Layer 4 exhausted — structured manual intervention response
                    $failed_count++;
                    \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] 🛑 manual_required — ' . $manual_reason);
                    $messages[] = [
                        'id'              => $entry_id,
                        'status'          => 'manual_required',
                        'broken_url'      => $entry['broken_url'],
                        'message'         => __('This link appears to be dynamically injected or hardcoded. Please modify manually.', 'seo-autofix-pro'),
                        'reason'          => $manual_reason,
                    ];
                } else {
                    // TRANSACTIONAL: Do NOT update table — leave as unfixed
                    $failed_count++;
                    \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] ❌ TRANSACTIONAL: NOT updating table — verification failed after ' . $attempts . ' attempts');
                    $messages[] = sprintf(
                        __('❌ Failed to fix: %s — verification failed after %d attempt(s)', 'seo-autofix-pro'),
                        esc_url($entry['broken_url']),
                        $attempts
                    );
                }
            } finally {
                // ALWAYS release lock
                if ($lock_key) {
                    delete_transient($lock_key);
                    \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] 🔓 Lock released for post ' . $post_id);
                }
            }
        }


        \SEOAutoFix_Debug_Logger::log('[APPLY_FIXES] Completed. Fixed: ' . $fixed_count . ', Failed: ' . $failed_count . ', Skipped: ' . $skipped_count);

        return array(
            'fixed_count' => $fixed_count,
            'failed_count' => $failed_count,
            'skipped_count' => $skipped_count,
            'messages' => $messages
        );
    }

    /**
     * Get post ID from URL with multiple fallback methods
     * Handles /index.php/ URLs and other WordPress URL formats
     * 
     * @param string $url The URL to get post ID from
     * @return int Post ID or 0 if not found
     */
    private function get_post_id_from_url($url)
    {
        \SEOAutoFix_Debug_Logger::log('[GET_POST_ID] Attempting to get post ID from URL: ' . $url);

        // Method 1: Standard WordPress url_to_postid()
        $post_id = url_to_postid($url);
        if ($post_id) {
            \SEOAutoFix_Debug_Logger::log('[GET_POST_ID] Method 1 (url_to_postid) succeeded: ' . $post_id);
            return $post_id;
        }

        // Method 2: Try removing /index.php/ if present
        if (strpos($url, '/index.php/') !== false) {
            $normalized_url = str_replace('/index.php/', '/', $url);
            \SEOAutoFix_Debug_Logger::log('[GET_POST_ID] Method 2: Trying without /index.php/: ' . $normalized_url);
            $post_id = url_to_postid($normalized_url);
            if ($post_id) {
                \SEOAutoFix_Debug_Logger::log('[GET_POST_ID] Method 2 succeeded: ' . $post_id);
                return $post_id;
            }
        }

        // Method 3: Try to extract page slug from URL
        $parsed = parse_url($url);
        if (isset($parsed['path'])) {
            // Remove trailing slash and leading slashes
            $path = trim($parsed['path'], '/');

            // Remove index.php if present
            $path = str_replace('index.php/', '', $path);

            // Get the last segment (page slug)
            $segments = explode('/', $path);
            $page_slug = end($segments);

            if (!empty($page_slug)) {
                \SEOAutoFix_Debug_Logger::log('[GET_POST_ID] Method 3: Extracted slug: ' . $page_slug);

                // Try to find page by slug
                $page = get_page_by_path($page_slug, OBJECT, array('post', 'page'));
                if ($page) {
                    \SEOAutoFix_Debug_Logger::log('[GET_POST_ID] Method 3 succeeded via get_page_by_path: ' . $page->ID);
                    return $page->ID;
                }

                // Try WP_Query as fallback
                \SEOAutoFix_Debug_Logger::log('[GET_POST_ID] Method 3b: Trying WP_Query with pagename');
                $query = new \WP_Query(array(
                    'pagename' => $page_slug,
                    'post_type' => array('post', 'page'),
                    'posts_per_page' => 1
                ));

                if ($query->have_posts()) {
                    $query->the_post();
                    $found_id = get_the_ID();
                    wp_reset_postdata();
                    \SEOAutoFix_Debug_Logger::log('[GET_POST_ID] Method 3b succeeded via WP_Query: ' . $found_id);
                    return $found_id;
                }
                wp_reset_postdata();
            }
        }

        // Method 4: Try to match against all pages/posts by URL
        \SEOAutoFix_Debug_Logger::log('[GET_POST_ID] Method 4: Querying all pages/posts to find URL match');
        global $wpdb;

        // Get all published posts and pages
        $posts = $wpdb->get_results(
            "SELECT ID FROM {$wpdb->posts} 
            WHERE post_status = 'publish' 
            AND post_type IN ('post', 'page')
            ORDER BY ID DESC
            LIMIT 500"
        );

        foreach ($posts as $post) {
            $post_url = get_permalink($post->ID);
            // Compare normalized URLs (without trailing slashes)
            if (untrailingslashit($post_url) === untrailingslashit($url)) {
                \SEOAutoFix_Debug_Logger::log('[GET_POST_ID] Method 4 succeeded by permalink match: ' . $post->ID);
                return $post->ID;
            }
        }

        \SEOAutoFix_Debug_Logger::log('[GET_POST_ID] All methods failed - could not find post ID');
        return 0;
    }

    /**
     * Public wrapper for get_post_id_from_url() — allows external classes to use
     * the robust multi-fallback URL-to-post-ID resolution.
     *
     * @param string $url The URL to resolve
     * @return int Post ID or 0 if not found
     */
    public function get_post_id_from_url_public($url)
    {
        return $this->get_post_id_from_url($url);
    }

    /**
     * Replace link in post/page content — 4-layer durable engine.
     *
     * Delegates entirely to Builder_Replacement_Engine which now implements
     * the full 4-layer strategy internally (Layer 1 builder, Layer 2 template,
     * Layer 3 postmeta deep-scan, Layer 4 global fallback).
     *
     * @param string $page_url        Page where link was found.
     * @param string $broken_url      Broken URL to replace.
     * @param string $replacement_url New URL.
     * @param string $location        Link location (content, header, footer, sidebar).
     * @param bool   &$manual_required Set to true if all layers fail.
     * @param string &$manual_reason   Human-readable reason for the failure.
     * @return bool Success.
     */
    private function replace_link_in_content(
        $page_url,
        $broken_url,
        $replacement_url,
        $location = 'content',
        &$manual_required = false,
        &$manual_reason = ''
    ) {
        \SEOAutoFix_Debug_Logger::log('[REPLACE_LINK] ===== replace_link_in_content() START =====');
        \SEOAutoFix_Debug_Logger::log('[REPLACE_LINK] Page URL     : ' . $page_url);
        \SEOAutoFix_Debug_Logger::log('[REPLACE_LINK] Broken URL   : ' . $broken_url);
        \SEOAutoFix_Debug_Logger::log('[REPLACE_LINK] Replacement  : ' . $replacement_url);
        \SEOAutoFix_Debug_Logger::log('[REPLACE_LINK] Location     : ' . $location);

        $post_id = $this->get_post_id_from_url($page_url);
        \SEOAutoFix_Debug_Logger::log('[REPLACE_LINK] Post ID from URL: ' . ($post_id ?: 'NOT FOUND'));

        if (!$post_id) {
            \SEOAutoFix_Debug_Logger::log('[REPLACE_LINK] ❌ Failed to get post ID from URL after all fallback attempts');
            return false;
        }

        // 4-layer engine — location-aware
        $builder_engine = new Builder_Replacement_Engine();
        $result         = $builder_engine->replace_url($post_id, $broken_url, $replacement_url, $location);

        \SEOAutoFix_Debug_Logger::log('[REPLACE_LINK] Builder engine result: success=' . ($result['success'] ? 'YES' : 'NO')
            . ' method=' . $result['method']
            . ' manual_required=' . ($result['manual_required'] ? 'YES' : 'NO'));

        if ($result['manual_required']) {
            $manual_required = true;
            $manual_reason   = $result['reason'];
        }

        \SEOAutoFix_Debug_Logger::log('[REPLACE_LINK] ===== replace_link_in_content() END =====');
        return $result['success'];
    }


    /**
     * Check if a link is template-generated and cannot be fixed programmatically
     * 
     * @param string $url The URL to check
     * @return bool True if template-generated
     */
    private function is_template_generated_link($url)
    {
        // Common patterns for template-generated links
        $template_patterns = array(
            '#respond',              // Comment reply links
            '#comment-',             // Comment anchors
            '/wp-admin/',           // Admin links
            '/wp-login.php',        // Login links
            '/wp-comments-post.php', // Comment form actions
            '/feed/',               // Feed links
            '?replytocom=',         // Reply to comment parameter
        );

        foreach ($template_patterns as $pattern) {
            if (strpos($url, $pattern) !== false) {
                \SEOAutoFix_Debug_Logger::log('[IS_TEMPLATE_GENERATED] URL contains pattern: ' . $pattern);
                return true;
            }
        }

        return false;
    }

    /**
     * Verify that a fix (URL replacement) was actually applied.
     * Builder-aware: checks the correct storage layer based on builder type.
     *
     * @param string $page_url     Page where link was found.
     * @param string $broken_url   URL that should no longer exist.
     * @param string $location     Location type (content, header, footer, sidebar).
     * @param string $builder_type Builder type from DB row (avoids re-detection).
     * @return bool True if broken URL no longer exists (fix was successful).
     */
    private function verify_fix_applied($page_url, $broken_url, $location = 'content', $builder_type = null)
    {
        \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] Verifying fix for broken URL: ' . $broken_url);
        \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] Page URL: ' . $page_url . ' | Location: ' . $location . ' | Builder: ' . ($builder_type ?: 'auto-detect'));

        $norm = Builder_Replacement_Engine::normalize_url($broken_url);

        // ── Header / Footer: check Elementor templates + nav menus ───────
        if (in_array($location, ['header', 'footer'], true)) {
            \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] Checking Elementor templates for location: ' . $location);

            $template_posts = get_posts([
                'post_type'   => 'elementor_library',
                'post_status' => 'publish',
                'numberposts' => -1,
                'meta_query'  => [[  'key' => '_elementor_template_type', 'value' => $location ]],
            ]);

            foreach ($template_posts as $tpl) {
                $raw = get_post_meta($tpl->ID, '_elementor_data', true);
                if (!empty($raw)) {
                    $norm_raw = Builder_Replacement_Engine::normalize_url($raw);
                    if (stripos($raw, $broken_url) !== false
                        || stripos($norm_raw, $norm) !== false) {
                        \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] ❌ Broken URL still exists in template #' . $tpl->ID);
                        return false;
                    }
                }
            }

            // Check nav menu items
            foreach (array_keys(get_registered_nav_menus()) as $loc) {
                $menu_id = get_nav_menu_locations()[$loc] ?? 0;
                if (!$menu_id) continue;
                $items = wp_get_nav_menu_items($menu_id, ['update_post_term_cache' => false]);
                if (empty($items)) continue;
                foreach ($items as $item) {
                    if (Builder_Replacement_Engine::normalize_url($item->url) === $norm) {
                        \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] ❌ Broken URL still in nav menu item #' . $item->ID);
                        return false;
                    }
                }
            }

            \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] ✅ Broken URL not found in templates/menus — fix verified');
            return true;
        }

        // ── Content / Sidebar: check post storage ─────────────────────────
        $post_id = $this->get_post_id_from_url($page_url);
        if (!$post_id) {
            \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] ⚠️ Could not get post ID — assuming fix was applied');
            return true;
        }

        // Flush WP object cache to force fresh DB read
        clean_post_cache($post_id);
        wp_cache_delete($post_id, 'post_meta');

        $builder = $builder_type ?? Builder_Detector::detect($post_id);
        \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] Using builder: ' . $builder);

        // --- Check post_content ---
        $post = get_post($post_id);
        if (!$post) {
            \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] ⚠️ Could not get post object — assuming fix was applied');
            return true;
        }

        $content = $post->post_content;
        \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] Fresh post_content length: ' . strlen($content));

        if (stripos($content, $broken_url) !== false
            || stripos($content, untrailingslashit($broken_url)) !== false) {
            \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] ❌ Broken URL still exists in post_content!');
            return false;
        }

        // --- Check Elementor storage ---
        if (in_array($builder, [Builder_Detector::ELEMENTOR, 'elementor'], true)) {
            $raw = get_post_meta($post_id, '_elementor_data', true);
            if (!empty($raw) && stripos($raw, $broken_url) !== false) {
                \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] ❌ Broken URL still exists in _elementor_data!');
                return false;
            }
            // Also check via normalized form
            if (!empty($raw) && stripos($raw, untrailingslashit($broken_url)) !== false) {
                \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] ❌ Broken URL (no-slash) still exists in _elementor_data!');
                return false;
            }
            \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] ✅ _elementor_data clear');
        }

        // --- Check all other post meta ---
        $skip_keys = [
            '_edit_lock', '_edit_last', '_wp_page_template', '_thumbnail_id',
            '_wp_attachment_metadata', '_wp_attached_file', '_encloseme', '_pingme',
            '_elementor_css', '_elementor_version', '_elementor_edit_mode',
            '_elementor_template_type', '_elementor_pro_version', '_elementor_data',
            '_elementor_page_settings',
        ];

        foreach ((get_post_meta($post_id) ?: []) as $meta_key => $meta_values) {
            if (in_array($meta_key, $skip_keys, true)) continue;
            foreach ($meta_values as $meta_value) {
                if (!is_string($meta_value)) continue;
                if (stripos($meta_value, $broken_url) !== false
                    || stripos($meta_value, untrailingslashit($broken_url)) !== false) {
                    \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] ❌ Broken URL still in meta key: ' . $meta_key);
                    return false;
                }
            }
        }

        \SEOAutoFix_Debug_Logger::log('[VERIFY_FIX] ✅ Broken URL not found anywhere — fix verified');
        return true;
    }

    /**
     * Verify that a link deletion was actually applied.
     * Checks BOTH URL absence AND anchor tag removal.
     *
     * @param string $page_url     Page where link was found.
     * @param string $broken_url   URL whose anchor should have been removed.
     * @param string $location     Location type.
     * @param string $builder_type Builder type from DB row.
     * @return bool True if anchor tag with URL has been removed.
     */
    private function verify_delete_applied($page_url, $broken_url, $location = 'content', $builder_type = null)
    {
        \SEOAutoFix_Debug_Logger::log('[VERIFY_DELETE] Verifying deletion of URL: ' . $broken_url);

        $post_id = $this->get_post_id_from_url($page_url);
        if (!$post_id) {
            \SEOAutoFix_Debug_Logger::log('[VERIFY_DELETE] ⚠️ Could not get post ID — assuming delete was applied');
            return true;
        }

        clean_post_cache($post_id);

        $builder = $builder_type ?? Builder_Detector::detect($post_id);
        $post = get_post($post_id);

        if (!$post) {
            \SEOAutoFix_Debug_Logger::log('[VERIFY_DELETE] ⚠️ Could not get post — assuming delete was applied');
            return true;
        }

        $content = $post->post_content;
        $escaped_url = preg_quote($broken_url, '/');

        // Check 1: Anchor tag with this URL should not exist
        $anchor_pattern = '/<a[^>]*href=["\']' . $escaped_url . '["\'][^>]*>/i';
        if (preg_match($anchor_pattern, $content)) {
            \SEOAutoFix_Debug_Logger::log('[VERIFY_DELETE] ❌ Anchor tag with URL still exists in post_content!');
            return false;
        }

        // Check 2: For Elementor, also check _elementor_data
        if ($builder === Builder_Detector::ELEMENTOR || $builder === 'elementor') {
            $raw = get_post_meta($post_id, '_elementor_data', true);
            if (!empty($raw) && stripos($raw, $broken_url) !== false) {
                \SEOAutoFix_Debug_Logger::log('[VERIFY_DELETE] ❌ URL still exists in _elementor_data after deletion!');
                return false;
            }
            \SEOAutoFix_Debug_Logger::log('[VERIFY_DELETE] ✅ URL not found in _elementor_data');
        }

        \SEOAutoFix_Debug_Logger::log('[VERIFY_DELETE] ✅ Anchor tag and URL absent — deletion verified');
        return true;
    }

