    {
        \SEOAutoFix_Debug_Logger::log('[LINK_ANALYZER] ==========  CONSTRUCTOR CALLED ==========');

        try {
            \SEOAutoFix_Debug_Logger::log('[LINK_ANALYZER] Creating Database_Manager...');
